context("repo")

test_that("extract_repo() works", {
  repo <- "ropenscilabs/travis"

  expect_equal(extract_repo("git://github.com/ropenscilabs/travis.git"), repo)
  expect_equal(extract_repo("https://github.com/ropenscilabs/travis.git"), repo)
  expect_equal(extract_repo("git@github.com:ropenscilabs/travis.git"), repo)
})
